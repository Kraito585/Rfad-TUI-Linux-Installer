#!/usr/bin/env bash

# Если директория не передана при запуске, берем текущую
TARGET_DIR=${1:-"."}
OUTPUT_FILE="hash_tree.txt"

echo "==> Сканирование директории: $TARGET_DIR"
echo "==> Сборка древа хэшей (может занять время для тяжелых папок)..."

# Удаляем старый файл, если он существует
rm -f "$OUTPUT_FILE"

# Как это работает:
# 1. find ищет все файлы (игнорируя директории)
# 2. -exec sha256sum {} + передает их пачкой (это работает в 50 раз быстрее, чем цикл while)
# 3. sed меняет местами стандартный вывод (ХЭШ ПУТЬ) на нужный тебе (ПУТЬ ХЭШ),
#    сохраняя при этом любые пробелы в названиях файлов.
find "$TARGET_DIR" -type f -exec sha256sum {} + | sed -E 's/^([a-f0-9]+)[[:space:]]+(.*)$/\2 \1/' > "$OUTPUT_FILE"

echo "==> Готово! Результат сохранен в $OUTPUT_FILE"
echo "==> Всего обработано файлов: $(wc -l < "$OUTPUT_FILE")"
